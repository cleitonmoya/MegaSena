    /**
     * Limpa todos os componentes do formulário
     */
    private void limpaFormulário(){
        
        // Limpa as marcações
        listaCheckBoxes.stream().forEach(jCheckBox -> {
                jCheckBox.setSelected(false);
                Font plainFont = new Font("Dialog", Font.PLAIN, 12);
                jCheckBox.setFont(plainFont);
                jCheckBox.setForeground(Color.black);
            }
        );
        
        // reinicializa variáveis auxiliares
        numSelecionados.clear();
        qtdeMarcaçõesFeitas = 0;
        
        if (jCheckBoxSurpresinha.isSelected()==false){
            atualizaMarcaçõesRestantes();
        }
        // limpa e a atualiza os labels
        jLabelAposta.setText("");
        atualizaProbabilidade();
        jLabelNúmerosSorteados.setText("");
        jLabelAcertos.setText("");
        jLabelResultado.setText("");
    }
    
    /**
     * Atualiza o número de marcações restantes para finalizar aposta;
     */
    private void atualizaMarcaçõesRestantes(){
        marcaçõesRestantes = qtdeNúmerosSelecionados - qtdeMarcaçõesFeitas;  
        lblMarcaçõesRestantes.setText(Integer.toString(marcaçõesRestantes));

        if (marcaçõesRestantes == 0) {
            jButtonApostar.setEnabled(true);
            jButtonTeimosinha.setEnabled(true);
        }
        else {
            jButtonApostar.setEnabled(false);
            jButtonTeimosinha.setEnabled(false);
        } 
    }
    
    /**
     * Atualiza a visualização dos números acertados
     */
    private void mostraAcertos(ArrayList<Integer> acertos){
        int qtdeAcertos = acertos.size();
        String resultado;
        switch (qtdeAcertos){
            case 0:
                resultado = "Chuta que é urucubaca";
                break;
            case 1:
                resultado = "Você acertou 1 número";
                break;
            case 4:
                resultado = "Parabéns, você acertou a quadra!";
                break;
            case 5:
                resultado = "Parabéns, você acertou a quina!";
                break;
            case 6:
                resultado = "PARABÉNS, VOCÊ AGORA É MILIONÁRIO!";
                break;
            default:
                resultado = "Você acertou "+qtdeAcertos+" números";
                break;
        }
        jLabelResultado.setText(resultado);
        
        // Marca os acertos em azul em caso de não supresinha
        if ((qtdeAcertos>0) && (jCheckBoxSurpresinha.isSelected()==false)){
            acertos.stream().forEach(acerto -> {
                listaCheckBoxes.get(acerto-1).setForeground(Color.blue);
            });
        } // if (qrdeAcertos>0)
        jLabelAcertos.setText(acertos.toString());
    } // private void mostraAcertos

    /**
     * Remove formatação de números acertados 
     */
    private void limpaAcertosAnteriores(){
        
        // Re-marca os números selecionados em vermelho em caso de não-surpresinha
        if ((acertos.size()>0) && (jCheckBoxSurpresinha.isSelected()==false)){
            numSelecionados.stream().forEach(num -> {
                listaCheckBoxes.get(num-1).setForeground(Color.red);
            });
            
        }
        // limpa a lista de acertos
        acertos.clear();
        jLabelAcertos.setText("");
    }
    
    private void atualizaProbabilidade(){
        long probabilidadeSena = Sena.calculaProbabilidade(qtdeNúmerosSelecionados,6);
        long probabilidadeQuina = Sena.calculaProbabilidade(qtdeNúmerosSelecionados,5);
        long probabilidadeQuadra = Sena.calculaProbabilidade(qtdeNúmerosSelecionados,4);
        DecimalFormat df = new DecimalFormat("###,###,###");
        lblProbablidadeSena.setText("1 em "+df.format(probabilidadeSena));
        lblProbablidadeQuina.setText("1 em "+df.format(probabilidadeQuina));
        lblProbablidadeQuadra.setText("1 em "+df.format(probabilidadeQuadra));
    }
    
