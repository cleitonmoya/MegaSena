    private void jCheckBoxSurpresinhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBoxSurpresinhaActionPerformed
        
        JCheckBox jCheckBox;
        jCheckBox = (JCheckBox) evt.getSource();
        
        limpaFormulário();
        if (jCheckBox.isSelected()){         
            // Desabilita os checkBox
            listaCheckBoxes.stream().forEach(checkBox -> {
                checkBox.setEnabled(false);
            });
            // Desabilita os labels de números restantes
            lblMarcaçõesRestantes.setText("");

            // Habilita a aposta
            jButtonApostar.setEnabled(true);
            jButtonTeimosinha.setEnabled(true);
            
        } else {
            // Habilita os checkBox
            listaCheckBoxes.stream().forEach(checkBox -> {
                checkBox.setEnabled(true); 
            });
            // Desabilita a aposta
            jButtonApostar.setEnabled(false);
            jButtonTeimosinha.setEnabled(false);
        }
    }//GEN-LAST:event_jCheckBoxSurpresinhaActionPerformed

    private class CheckBoxHandler implements ActionListener{
        @Override
        public void actionPerformed(ActionEvent event){
            JCheckBox jCheckBox;
            jCheckBox = (JCheckBox) event.getSource();

                if (jCheckBox.isSelected()){
                    if (marcaçõesRestantes > 0){
                        jCheckBox.setForeground(Color.red);
                        Font boldFont = new Font("Dialog", Font.BOLD, 12);
                        jCheckBox.setFont(boldFont);
                        numSelecionados.add(Integer.parseInt(jCheckBox.getText()));
                        qtdeMarcaçõesFeitas++;
                    } else {
                        jCheckBox.setSelected(false);
                       // JOptionPane.showMessageDialog(rootPane, "Você selecionou mais números do que o definido para esta aposta");
                    }
                       
                } else {
                    Font plainFont = new Font("Dialog", Font.PLAIN, 12);
                    jCheckBox.setFont(plainFont);
                    jCheckBox.setForeground(Color.black);
                    numSelecionados.remove(Integer.parseInt(jCheckBox.getText()));        
                    qtdeMarcaçõesFeitas--;
                }              
                atualizaMarcaçõesRestantes();      
        }
    }
    
    
