    private void jButtonApostarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonApostarActionPerformed
 
        // Sorteia os números em caso de Surpresinha
        if (jCheckBoxSurpresinha.isSelected()){
            numSelecionados.clear();
            numSelecionados.addAll(sena.sorteiaNumeros(qtdeNúmerosSelecionados));
        }

        // Mostra os números selecionados
        ArrayList<Integer> selecionados = new ArrayList<>(numSelecionados);
        Collections.sort(selecionados);
        jLabelAposta.setText(selecionados.toString());
        
        // Limpa os acertos anteriores
        limpaAcertosAnteriores();
         
        // Sorteia os números da Mega-Sena
        numSorteados = sena.sorteiaNumeros(6);
        jLabelNúmerosSorteados.setText(numSorteados.toString());
        
        // Identifica os números que foram acertados
        for (int i=0; i<6;i++){
            if (numSelecionados.contains(numSorteados.get(i)))
                acertos.add(numSorteados.get(i));
        }
       
        // Mostra os números acertados
        mostraAcertos(acertos);
    }//GEN-LAST:event_jButtonApostarActionPerformed
   
    private void cmbQtdeNúmerosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbQtdeNúmerosActionPerformed
        qtdeNúmerosSelecionados = Integer.parseInt(cmbQtdeNúmeros.getSelectedItem().toString());
        
        if (jCheckBoxSurpresinha.isSelected()== false){
            if (qtdeNúmerosSelecionados >= qtdeMarcaçõesFeitas)
                atualizaMarcaçõesRestantes();
            else
                limpaFormulário();
        }
        
        // Atutaliza a probabilidade
        atualizaProbabilidade();
        
    }//GEN-LAST:event_cmbQtdeNúmerosActionPerformed

